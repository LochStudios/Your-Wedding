        <link rel="stylesheet" href="style.css?v=<?php echo uuidv4(); ?>" />
    </head>
    <body>
        <?php include_once __DIR__ . '/nav.php'; ?>
        <section class="section full-bleed full-height">
            <div class="container is-fluid">
                <h1 class="title">Analytics Report</h1>
                <?php if ($sent): ?>
                    <div class="notification is-success">
                        Report successfully sent to <?php echo htmlspecialchars($clientEmail); ?>
                    </div>
                <?php else: ?>
                    <div class="notification is-warning">
                        <?php echo htmlspecialchars($_SESSION['admin_flash'] ?? 'Unable to send email.'); ?>
                    </div>
                <?php endif; ?>
                <div class="box">
                    <h2 class="title is-5">Report Preview</h2>
                    <pre class="report-preview"><?php echo htmlspecialchars($emailBody); ?></pre>
                </div>
                <div class="buttons">
                    <a href="analytics.php?album_id=<?php echo $albumId; ?>&date_from=<?php echo urlencode($dateFrom); ?>&date_to=<?php echo urlencode($dateTo); ?>" class="button is-link">Back to Analytics</a>
                    <a href="dashboard.php" class="button is-light">Dashboard</a>
                </div>
            </div>
